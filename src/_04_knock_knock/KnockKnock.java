package _04_knock_knock;

import javax.swing.JOptionPane;

public class KnockKnock {
public static void main(String[] args) {
	JOptionPane.showMessageDialog(null,"Knock Knock");
	JOptionPane.showMessageDialog(null, "Who's there?");
	JOptionPane.showMessageDialog(null,"Cow says");
	JOptionPane.showMessageDialog(null,"Cow says who?");
	JOptionPane.showMessageDialog(null,"Cows don't say who, they say moo!");
}
}
